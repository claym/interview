package net.claymitchell.neetcode.linkedlist;

public class ListKeyNode {
    public int key;
    public int val;
    public ListKeyNode next;
    public ListKeyNode prev;

    public ListKeyNode() {
        super();
    }

    public ListKeyNode(int key, int val) {
        this.key = key;
        this.val = val;
    }
}
